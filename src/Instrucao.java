public class Instrucao {
    public String tipo;
    public String nomeInstrucao;
    public String hexa;
    public String binario;

    public Instrucao(String tipo, String nomeInstrucao, String hexa, String binario) {
        this.tipo = tipo;
        this.nomeInstrucao = nomeInstrucao;
        this.hexa = hexa;
        this.binario = binario;
    }


}
